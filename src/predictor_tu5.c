/* Максимальная размерность для стековых массивов (ПРГ:Ту-5.3) */
const uint32_t PREDICTOR_MAX_DIM = 100;

/* Функция проверки размерности (ПРГ:Ту-5.2, 5.3) */
bool Pred_IsDimensionValid(uint32_t n, uint32_t p) {
    return (n > 0 && p > 0 && n <= PREDICTOR_MAX_DIM && p <= PREDICTOR_MAX_DIM);
}
